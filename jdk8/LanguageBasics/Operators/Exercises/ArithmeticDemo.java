package LanguageBasics.Operators.Exercises;

public class ArithmeticDemo {
    public void arithmeticDemo() {
        int result = 3;
        System.out.println(result);

        result -= 1;
        System.out.println(result);

        result *= 2;
        System.out.println(result);

        result /= 2;
        System.out.println(result);

        result += 8;
        result %= 7;
        System.out.println(result);
    }
}
